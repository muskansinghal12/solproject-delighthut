document.querySelector(".cart").addEventListener("click", function() {
  var price = 0;
  document.querySelector(".modal-body").innerHTML = "<tr><th style='padding-left: 5%';>Item</th><th>Price</th></tr>";
  menu.forEach((values, keys) => {
    price = price + values;
    document.querySelector(".modal-body").innerHTML += "<tr><td style='padding-left: 5%';>" + keys + "</td><td>" + values + "</td></tr>";
  })
  if (menu.size != 0) {
    document.querySelector(".modal-body").innerHTML += "<td style='font-weight:bold;padding-left: 5%';>" + "Total Amount" + "</td><td style='font-weight:bold;'>" + price + "</td></tr>";
    if (price > 2000) {
      document.querySelector(".modal-body").innerHTML += "<td style='font-weight:bold; padding-left: 5%';>" + "Discount: (20% above Rs. 2000)" + "</td><td>" + "-" + 0.2 * price + "</td></tr>";
      price = price - 0.2 * price;
      document.querySelector(".modal-body").innerHTML += "<td style='font-weight:bold; padding-left: 5%';>" + "Final Amount" + "</td><td style='font-weight:bold;'>" + price + "</td></tr>";

    }
    document.querySelector(".order-form").setAttribute("action", "/form");
  } else {
    document.querySelector(".modal-body").innerHTML = "<center><em><h2>Your Cart is Empty</h2></em></center>";
    document.querySelector(".order-form").setAttribute("action", "/menu");

  }
  var keys = Array.from(menu.keys());
  var a = JSON.stringify(keys);
  //alert(a);
  document.querySelector(".purchased-item").setAttribute("value", a);
  document.querySelector(".amount").setAttribute("value", price);




});
